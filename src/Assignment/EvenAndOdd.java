package Assignment;

public class EvenAndOdd {

	public static void main(String[] args) {
		int a=10;
		if(a%2==0) {
			System.out.println("a is Even Number");
		}else {
			System.out.println("a is Odd Number");
		}
		
		int b=1;
		if(b%2==0) {
			System.out.println("b is Even Number");
		}else {
			System.out.println("b is Odd Number");
		}
	}

}
